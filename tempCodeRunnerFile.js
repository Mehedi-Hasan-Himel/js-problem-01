/* 
Even number of array average.
*/

function evenAverage(numbers) {
  const even = [];
  for (number of numbers) {
    if (number % 2 === 0) {
      console.log(number);

      even.push(number);
    }
  }
  console.log(even);
}

const numbers = [42, 13, 58, 65, 81, 96, 7, 5];
const avg = evenAverage(numbers);

console.log(avg);
